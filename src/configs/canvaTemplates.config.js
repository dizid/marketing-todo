/**
 * Build a Canva edit link with color parameters
 * @param {string} templateUrl - Base Canva template URL
 * @param {string} brandColor - Brand color in hex format (e.g., '#6366F1')
 * @returns {string} URL with color parameters
 */
export const buildCanvaLink = (templateUrl, brandColor = null) => {
  // Extract template ID from URL if needed
  if (!templateUrl.includes('canva.com')) {
    return templateUrl
  }

  // Add color parameter if provided (Canva supports color customization in URLs)
  if (brandColor) {
    const cleanColor = brandColor.replace('#', '')
    return `${templateUrl}?color=${cleanColor}`
  }

  return templateUrl
}

/**
 * Get templates for a specific design purpose
 * @param {string} purposeId - The design purpose ID (e.g., 'social-banner')
 * @returns {Array} Array of template objects
 */
export const getTemplatesForPurpose = (purposeId) => {
  return canvaTemplates[purposeId] || []
}

/**
 * Get a random template for a given purpose
 * @param {string} purposeId - The design purpose ID
 * @returns {Object|null} Random template object
 */
export const getRandomTemplate = (purposeId) => {
  const templates = getTemplatesForPurpose(purposeId)
  if (templates.length === 0) return null
  return templates[Math.floor(Math.random() * templates.length)]
}

/**
 * Filter templates by style
 * @param {string} purposeId - The design purpose ID
 * @param {string} styleId - The design style ID
 * @returns {Array} Filtered templates matching both criteria
 */
export const filterTemplatesByStyle = (purposeId, styleId) => {
  const templates = getTemplatesForPurpose(purposeId)
  return templates.filter(t => t.style === styleId)
}
